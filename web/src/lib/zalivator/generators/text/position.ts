const ALL_POSITIONS = Object.values(POSITION_BY_DOMAIN).flat()

function getDomainPositions(domain: ZalivatorPositionDomain) {
  if (domain === "any") {
    return ALL_POSITIONS
  }

  return POSITION_BY_DOMAIN[domain]
}

export function generatePosition(options: ZalivatorPositionOptions) {
  return pickRandom([...getDomainPositions(options.domain)])
}
